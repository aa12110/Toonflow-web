<template>
  <div class="themeConfig">
    <t-form label-align="top">
      <!-- 主题模式切换 -->
      <t-form-item label="主题模式">
        <t-radio-group
          v-model="themeSetting.mode"
          variant="default-filled"
          @change="(val, ctx) => handleModeChange(val as string, ctx.e as MouseEvent)">
          <t-radio-button value="light">
            <template #label>
              <t-icon name="sunny" />
              浅色
            </template>
          </t-radio-button>
          <t-radio-button value="dark">
            <template #label>
              <t-icon name="moon" />
              深色
            </template>
          </t-radio-button>
          <t-radio-button value="auto">
            <template #label>
              <t-icon name="desktop" />
              跟随系统
            </template>
          </t-radio-button>
        </t-radio-group>
      </t-form-item>

      <!-- 主题色选择 -->
      <t-form-item label="主题色">
        <div class="colorList">
          <div
            v-for="color in themeColors"
            :key="color.value"
            class="colorItem"
            :class="{ active: themeSetting.primaryColor === color.value }"
            :style="{ backgroundColor: color.value }"
            @click="(e: MouseEvent) => handleColorChange(color.value, e)">
            <t-icon v-if="themeSetting.primaryColor === color.value" name="check" class="checkIcon" />
          </div>
          <t-color-picker
            v-model="themeSetting.primaryColor"
            format="HEX"
            :color-modes="['monochrome']"
            :show-primary-color-preview="false"
            @change="(val: string) => handleColorChange(val)">
            <template #trigger>
              <div class="colorItem customColor" :style="{ backgroundColor: isCustomColor ? themeSetting.primaryColor : '#fff' }">
                <t-icon :name="isCustomColor ? 'check' : 'edit'" :class="['customIcon', { checkIcon: isCustomColor }]" />
              </div>
            </template>
          </t-color-picker>
        </div>
      </t-form-item>
    </t-form>
  </div>
</template>

<script setup lang="ts">
import { computed, onMounted, watch } from "vue";
import settingStore from "@/stores/setting";

const store = settingStore();
const { themeSetting } = storeToRefs(store);

// 预设主题色
const themeColors = [
  { name: "香芋紫", value: "#9810fa" },
  { name: "活力橙", value: "#ED7B2F" },
  { name: "柠檬黄", value: "#F5BA18" },
  { name: "薄荷绿", value: "#00A870" },
  { name: "青翠绿", value: "#2BA471" },
  { name: "山茶红", value: "#D54941" },
  { name: "碧空蓝", value: "#029CD4" },
];

// 判断是否为自定义颜色
const isCustomColor = computed(() => !themeColors.some((c) => c.value.toLowerCase() === themeSetting.value.primaryColor.toLowerCase()));

// 处理主题模式切换
const handleModeChange = (mode: string, event?: MouseEvent) => {
  toggleThemeWithTransition(event, () => applyThemeMode(mode));
};

// 使用 View Transition API 进行平滑过渡
const toggleThemeWithTransition = (event: MouseEvent | undefined, callback: () => void) => {
  if (!document.startViewTransition) {
    callback();
    return;
  }

  const x = event?.clientX ?? window.innerWidth / 2;
  const y = event?.clientY ?? window.innerHeight / 2;
  const endRadius = Math.hypot(Math.max(x, window.innerWidth - x), Math.max(y, window.innerHeight - y));

  const root = document.documentElement;
  root.style.setProperty("--x", `${x}px`);
  root.style.setProperty("--y", `${y}px`);
  root.style.setProperty("--r", `${endRadius}px`);

  document.startViewTransition(callback);
};

// 应用主题模式
const applyThemeMode = (mode: string) => {
  const targetMode = mode === "auto" ? (window.matchMedia("(prefers-color-scheme: dark)").matches ? "dark" : "light") : mode;
  document.documentElement.setAttribute("theme-mode", targetMode);
};

// 处理主题色变更
const handleColorChange = (color: string, event?: MouseEvent) => {
  themeSetting.value.primaryColor = color;
  toggleThemeWithTransition(event, () => applyThemeColor(color));
};

// HEX 转 HSL
const hexToHsl = (hex: string) => {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
  if (!result) return { h: 0, s: 0, l: 0 };

  const r = parseInt(result[1], 16) / 255;
  const g = parseInt(result[2], 16) / 255;
  const b = parseInt(result[3], 16) / 255;
  const max = Math.max(r, g, b),
    min = Math.min(r, g, b);
  const l = (max + min) / 2;
  let h = 0,
    s = 0;

  if (max !== min) {
    const d = max - min;
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
    if (max === r) h = ((g - b) / d + (g < b ? 6 : 0)) / 6;
    else if (max === g) h = ((b - r) / d + 2) / 6;
    else h = ((r - g) / d + 4) / 6;
  }

  return { h: Math.round(h * 360), s: Math.round(s * 100), l: Math.round(l * 100) };
};

// HSL 转 HEX
const hslToHex = (h: number, s: number, l: number) => {
  s /= 100;
  l /= 100;
  const c = (1 - Math.abs(2 * l - 1)) * s;
  const x = c * (1 - Math.abs(((h / 60) % 2) - 1));
  const m = l - c / 2;

  let r = 0,
    g = 0,
    b = 0;
  if (h < 60) [r, g, b] = [c, x, 0];
  else if (h < 120) [r, g, b] = [x, c, 0];
  else if (h < 180) [r, g, b] = [0, c, x];
  else if (h < 240) [r, g, b] = [0, x, c];
  else if (h < 300) [r, g, b] = [x, 0, c];
  else [r, g, b] = [c, 0, x];

  const toHex = (n: number) =>
    Math.round((n + m) * 255)
      .toString(16)
      .padStart(2, "0");
  return `#${toHex(r)}${toHex(g)}${toHex(b)}`;
};

// 生成品牌色阶
const generateColorPalette = (hex: string) => {
  const { h, s, l } = hexToHsl(hex);
  const lightLevels = [97, 92, 85, 75, 62, l, Math.max(l - 12, 20), Math.max(l - 24, 15), Math.max(l - 36, 10), Math.max(l - 48, 5)];
  return lightLevels.map((level) => hslToHex(h, s, level));
};

// 应用主题色
const applyThemeColor = (color: string) => {
  const root = document.documentElement;
  const palette = generateColorPalette(color);
  const isDark = root.getAttribute("theme-mode") === "dark";
  const colors = isDark ? [...palette].reverse() : palette;

  colors.forEach((c, i) => root.style.setProperty(`--td-brand-color-${i + 1}`, c));

  ["", "-hover:5", "-focus:2", "-active:7", "-disabled:3", "-light:1", "-light-hover:2"].forEach((suffix) => {
    const [name, level] = suffix.split(":");
    root.style.setProperty(`--td-brand-color${name}`, level ? `var(--td-brand-color-${level})` : "var(--td-brand-color-6)");
  });

  root.style.setProperty("--td-text-color-brand", `var(--td-brand-color-${isDark ? 8 : 7})`);
  root.style.setProperty("--td-text-color-link", "var(--td-brand-color-8)");
};

// 监听系统主题变化
window.matchMedia("(prefers-color-scheme: dark)").addEventListener("change", (e) => {
  if (themeSetting.value.mode === "auto") {
    toggleThemeWithTransition(undefined, () => {
      document.documentElement.setAttribute("theme-mode", e.matches ? "dark" : "light");
      applyThemeColor(themeSetting.value.primaryColor);
    });
  }
});

// 初始化
onMounted(() => {
  applyThemeMode(themeSetting.value.mode);
  applyThemeColor(themeSetting.value.primaryColor);
});

// 监听主题模式变化
watch(
  () => themeSetting.value.mode,
  () => setTimeout(() => applyThemeColor(themeSetting.value.primaryColor), 0),
);
</script>

